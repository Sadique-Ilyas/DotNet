using System;

namespace BasicMathOperations
{
	public class BasicMath
	{
		static void Main()
		{
			
        }

		public int Add(int x, int y)
		{
			return x + y;
		}

		public int Subtract(int x, int y)
		{
			return x - y;
		}

		public int Multiply(int x, int y)
		{
			return x * y;
		}

		public int Divide(int x, int y)
		{
			return x / y;
		}
	}
}
